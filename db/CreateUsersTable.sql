CREATE TABLE Pet
(
pid CHAR(16),
uid CHAR(16),
name CHAR(10),
sex CHAR(1),
birthDate DATE,
regDate DATE,
breed CHAR(20),
imageURL CHAR(100),
PRIMARY KEY (pid)
FOREIGN KEY(uid) REFERENCES User
ON DELETE NO ACTION
);


CREATE TABLE Lost
(
pid CHAR(16),
lostDate CHAR(6),
lostTime CHAR(6),
lostLat FLOAT,
lostLong FLOAT,
PRIMARY KEY (pid),
FOREIGN KEY (pid) REFERENCES Pet
);
