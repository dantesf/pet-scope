CREATE TABLE Users(
    userid SERIAL,
	username VARCHAR(32) UNIQUE NOT NULL,
	password TEXT NOT NULL,
	isActive BOOLEAN NOT NULL DEFAULT TRUE,
	PRIMARY KEY (userid)
);


